var articulo1 = document.getElementById("shop__article");
var afectado1 = document.getElementById("shop__picture");
articulo1.addEventListener("mouseover", () => {
  afectado1.className =  "shop__pictures afectado";
}, false)
articulo1.addEventListener("mouseout", () => {
  afectado1.className = "shop__pictures";
}, false)


